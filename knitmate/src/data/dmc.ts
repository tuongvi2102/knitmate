// Deduplicate by id (keep first occurrence)
const _seenIds = new Set<string>();
export const DMC = DMC_RAW.filter(([id]) => {
  if (_seenIds.has(id)) return false;
  _seenIds.add(id);
  return true;
});

export interface DmcColor {
  id: string;
  name: string;
  r: number;
  g: number;
  b: number;
  lab: [number, number, number];
}

export const DMC_WITH_LAB: DmcColor[] = DMC.map(([id, name, r, g, b]) => ({
  id, name, r, g, b, lab: rgbToLab(r, g, b),
}));

const _colorCache = new Map<string, number>();

export function nearestDMCIndex(r: number, g: number, b: number): number {
  const key = `${r},${g},${b}`;
  if (_colorCache.has(key)) return _colorCache.get(key)!;
  const lab = rgbToLab(r, g, b);
  let best = 0, bestDist = Infinity;
  for (let i = 0; i < DMC_WITH_LAB.length; i++) {
    const d = labDistance(lab, DMC_WITH_LAB[i].lab);
    if (d < bestDist) { bestDist = d; best = i; }
  }
  _colorCache.set(key, best);
  return best;
}
